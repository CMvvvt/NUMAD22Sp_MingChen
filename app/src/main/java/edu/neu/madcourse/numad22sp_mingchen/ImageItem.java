package edu.neu.madcourse.numad22sp_mingchen;

public class ImageItem {
    private String name;
    private String url;

    public ImageItem(String name, String url) {
        this.name = name;
        this.url = url;
    }

    public String getName() {
        return name;
    }

    public String getUrl() {
        return url;
    }
}
